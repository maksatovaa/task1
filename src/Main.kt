fun main() {
    val valOne = readln().toInt()
    val valTwo = readln().toInt()

    val valSumma = valOne + valTwo
    val valSub = valOne - valTwo
    val valMult = valOne * valTwo
    val valDel = valOne / valTwo
    println("Сумма равна $valSumma")
    println("вычитание равно $valSub")
    println("множение равно $valMult")
    println("деление равно $valDel")
}